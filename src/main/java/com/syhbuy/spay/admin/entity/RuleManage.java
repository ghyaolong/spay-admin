package com.syhbuy.spay.admin.entity;

public class RuleManage {
	
	   private  int ID;
	   private String ruleTitle;//规则标题
	   private String ruleDirection;//规则指向
	   private float rulePercent;//返还百分比
	   private String ruleTrigger;//触发条件
	   private int ruleDelay;//触发延迟时间
	   private String ruleTo;//规则指向
	   private String createTime;//创建时间
	   private int inUse;//使用位置个数
	   private int ruleState;//使用状态
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getRuleTitle() {
		return ruleTitle;
	}
	
	public void setRuleTitle(String ruleTitle) {
		this.ruleTitle = ruleTitle;
	}
	public String getRuleDirection() {
		return ruleDirection;
	}
	public void setRuleDirection(String ruleDirection) {
		this.ruleDirection = ruleDirection;
	}
	public float getRulePercent() {
		return rulePercent;
	}
	public void setRulePercent(float rulePercent) {
		this.rulePercent = rulePercent;
	}
	public String getRuleTrigger() {
		return ruleTrigger;
	}
	public void setRuleTrigger(String ruleTrigger) {
		this.ruleTrigger = ruleTrigger;
	}
	public int getRuleDelay() {
		return ruleDelay;
	}
	public void setRuleDelay(int ruleDelay) {
		this.ruleDelay = ruleDelay;
	}
	public String getRuleTo() {
		return ruleTo;
	}
	public void setRuleTo(String ruleTo) {
		this.ruleTo = ruleTo;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public int getIn_use() {
		return inUse;
	}
	public void setIn_use(int inUse) {
		this.inUse = inUse;
	}
	public int getRuleState() {
		return ruleState;
	}
	public void setRule_state(int ruleState) {
		this.ruleState = ruleState;
	}
	@Override
	public String toString() {
		return "ManageRule [ID=" + ID + ", ruleTitle=" + ruleTitle
				+ ", ruleDirection=" + ruleDirection + ", rulePercent="
				+ rulePercent + ", ruleTrigger=" + ruleTrigger + ", ruleDelay="
				+ ruleDelay + ", ruleTo=" + ruleTo + ", createTime="
				+ createTime + ", inUse=" + inUse + ", ruleState=" + ruleState
				+ "]";
	}
	
	
	
	

}
